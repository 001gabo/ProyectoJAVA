    private void verlistado(){
        String llamadaproc;
        DefaultTableModel modelo= new DefaultTableModel();  
        modelo=(DefaultTableModel)this.tabla_morosos.getModel();
        String []array = new String[6];
        
        try{
            con=conexion_mysql.getConnection();  
            llamadaproc="call PA_Moras()";
            st=con.createStatement();
            rs=st.executeQuery(llamadaproc);
            while(rs.next()){
                array[0]=rs.getString(1);
                array[1]=rs.getString(2);
                array[2]=rs.getString(3);
                array[3]=rs.getString(4);
                array[4]=rs.getString(5); 
                array[5]=rs.getString(6); 
                modelo.addRow(array);
                System.out.println("Esta es una linea");
            }
            tabla_morosos.setModel(modelo); 
            this.revalidate();
            this.repaint();
            
        }catch(Exception ex){ 
          JOptionPane.showMessageDialog(this,"Ocurrio un error: "+ex);
        }finally{
            try{
                st.close();
                rs.close();
                con.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,"Ocurrio un error: "+e);
            }
        }
    }
    
